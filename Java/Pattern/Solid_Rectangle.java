// Write a program to 4x4 Solid_Rectangle Pattern

package Pattern;

public class Solid_Rectangle 
{
    public static void main(String[] args) {
        for(int i=1;i<=4;i++)
        {
            for(int j=1;j<=4;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
